class Immobile {
	constructor(
		id,
		indirizzo,
		comune,
		nome,
		tipologia,
		tipoAnnuncio,
		descrizione,
		immagini,
		prezzo,
		grandezza,
		attivo
	) {
		this.id = id;
		this.indirizzo = indirizzo;
		this.comune = comune;
		this.nome = nome;
		this.tipologia = tipologia;
		this.tipoAnnuncio = tipoAnnuncio;
		this.descrizione = descrizione;
		this.prezzo = prezzo;
		this.grandezza = grandezza;
		this.attivo = attivo;
		this.immagini = immagini;
	}
}
